import SwiftUI

extension Font {
  static func jakartaRegular(size: CGFloat) -> Font {
    return Font.custom("PlusJakartaSans-Regular", size: size)
  }
  
  static func jakartaBold(size: CGFloat) -> Font {
    return Font.custom("PlusJakartaSans-Bold", size: size)
  }
  
  static func jakartaMedium(size: CGFloat) -> Font {
    return Font.custom("PlusJakartaSans-Medium", size: size)
  }
}
